#include "../../includes/rt.h"

float	closest_distance(t_vec4 d)
{
	float	t;

	t = (-d.y - sqrt(d.w)) / (2.0 * d.x);
	if (t < 0)
		t = (-d.y + sqrt(d.w)) / (2.0 * d.x);
	return (t);
}
